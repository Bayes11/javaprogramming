package day07_operators;

public class shorthandOperators {
    public static void main(String[] args) {
        int y = 300;

        y%=16;

        System.out.println("y = " + y);

        int x = 200;

        x+=25;

        System.out.println("x = " + x);

        int z = 100;

        z-=50;

        System.out.println("z = " + z);

        int v = 50;

        v/=10;

        System.out.println("v = " + v);
    }
}
