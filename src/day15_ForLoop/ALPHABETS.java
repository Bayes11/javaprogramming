package day15_ForLoop;

public class ALPHABETS {
    public static void main(String[] args) {
        for (int i = 97; i <= 122; i++) {
            System.out.print((char) i + " ");
        }

        System.out.println();
        for (int j = 65; j <= 90; j++)
            System.out.print((char) j + " ");

    }
}